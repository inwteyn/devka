class Touch_Form_Classified_Photo_Delete extends Touch_Form_Standard
{
  public function init()
  {
    $this
      ->setTitle('TOUCH_Delete Classified Photo')
      ->setDescription('TOUCH_Are you sure you want to delete this photo?')
      ;

    $this->addElement('Button', 'submit', array(
      'type' => 'submit',
      'label' => 'Delete Photo',
      'decorators' => array('ViewHelper')
    ));

    $this->addElement('Cancel', 'cancel', array(
      'label' => 'cancel',
      'link' => true,
      'prependText' => ' or ',
      'href' => '',
      'onclick' => 'parent.Smoothbox.close();',
      'decorators' => array(
        'ViewHelper'
      )
    ));
    $this->addDisplayGroup(array('submit', 'cancel'), 'buttons');
  }
}